  let lang = 'de';

  function setLang(next) {
    lang = translations[next] ? next : 'de';
  }

  function getLang() {
    return lang;
  }

  function t(key, vars) {
    const table = translations[lang] || translations.en;
    let str = table[key];
    if (str === undefined) str = translations.en[key];
    if (str === undefined) str = key;
    if (vars) {
      str = str.replace(/\{(\w+)\}/g, (match, name) => (vars[name] !== undefined ? String(vars[name]) : match));
    }
    return str;
  }

  // Apply translations to static markup. Elements opt in via attributes:
  //   data-i18n        -> textContent
  //   data-i18n-html   -> innerHTML (for strings containing markup)
  //   data-i18n-ph     -> placeholder attribute
  //   data-i18n-title  -> title attribute
  // FAQ prose lives in two sibling blocks tagged data-faq-lang; only the
  // active language is shown.
  function applyStatic(root) {
    const scope = root || document;
    scope.querySelectorAll('[data-i18n]').forEach((el) => {
      el.textContent = t(el.getAttribute('data-i18n'));
    });
    scope.querySelectorAll('[data-i18n-html]').forEach((el) => {
      el.innerHTML = t(el.getAttribute('data-i18n-html'));
    });
    scope.querySelectorAll('[data-i18n-ph]').forEach((el) => {
      el.setAttribute('placeholder', t(el.getAttribute('data-i18n-ph')));
    });
    scope.querySelectorAll('[data-i18n-title]').forEach((el) => {
      el.setAttribute('title', t(el.getAttribute('data-i18n-title')));
    });
    scope.querySelectorAll('[data-faq-lang]').forEach((el) => {
      el.hidden = el.getAttribute('data-faq-lang') !== lang;
    });
    document.documentElement.lang = lang;
  }

  window.I18N = { t, setLang, getLang, applyStatic };
